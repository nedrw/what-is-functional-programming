//! 函数式编程演示项目
//!
//! 通过四个示例展示 Rust 中函数式编程的核心思想：
//!
//! - **数据筛选** (`data_filtering`): 迭代器组合子 filter/map/fold
//! - **并行计算** (`parallel_compute`): 不可变性带来的并发优势
//! - **错误处理** (`error_handling`): Result/Option 的链式组合
//! - **状态机** (`state_machine`): 纯函数定义状态转换
//!
//! 运行示例：
//! ```sh
//! cargo run --example data_filtering
//! cargo run --example parallel_compute
//! cargo run --example error_handling
//! cargo run --example state_machine
//! ```
